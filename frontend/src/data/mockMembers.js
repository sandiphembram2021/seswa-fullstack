// Mock API response structure
export const createMockResponse = (page = 1, limit = 12, filters = {}) => {
  let filteredMembers = [...mockMembers];

  // Apply filters
  if (filters.search) {
    const searchTerm = filters.search.toLowerCase();
    filteredMembers = filteredMembers.filter(member =>
      member.firstName.toLowerCase().includes(searchTerm) ||
      member.lastName.toLowerCase().includes(searchTerm) ||
      member.college?.toLowerCase().includes(searchTerm) ||
      member.branch?.toLowerCase().includes(searchTerm) ||
      member.district.toLowerCase().includes(searchTerm)
    );
  }

  if (filters.role) {
    filteredMembers = filteredMembers.filter(member => member.role === filters.role);
  }

  if (filters.college) {
    filteredMembers = filteredMembers.filter(member => member.college === filters.college);
  }

  if (filters.branch) {
    filteredMembers = filteredMembers.filter(member => member.branch === filters.branch);
  }

  if (filters.district) {
    filteredMembers = filteredMembers.filter(member => member.district === filters.district);
  }

  // Pagination
  const total = filteredMembers.length;
  const pages = Math.ceil(total / limit);
  const startIndex = (page - 1) * limit;
  const endIndex = startIndex + limit;
  const paginatedMembers = filteredMembers.slice(startIndex, endIndex);

  return {
    success: true,
    count: paginatedMembers.length,
    pagination: {
      page,
      limit,
      total,
      pages
    },
    data: {
      users: paginatedMembers
    }
  };
};
